:fibonacci
MOV CL, 08H
MOV SI,1000H

MOV AL,00H
MOV [SI], AL
INC SI

MOV AL,01H
MOV [SI],AL

L1:DEC SI
ADD AL, [SI]
INC SI
INC SI
MOV [SI], AL
DEC CL
JNZ L1

;sq of fibonacci series
MOV CL,0AH
MOV SI,1000H
MOV DI,2000H

L2:MOV AL,[SI]
MOV BL,AL
MUL BL
MOV [DI],AX
INC SI
INC DI
INC DI
DEC CL
JNZ L2

HLT
